package uk.co.dubit.couch;

/**
 * A request processing exception is an exception that is thrown if a problem is encountered whilst forming the request that will prevent it from being sent. This includes character encoding problems, and server connection failures.
 * @author Thomas Williams on behalf of dubitlimited.com
 */
public class RequestProcessingException extends RuntimeException {
	
	private static final long serialVersionUID = 2570720449561218090L;

	public RequestProcessingException(final String message, final Throwable cause) {
		super(message,cause);
	}
	
	public RequestProcessingException(final String message) {
		super(message);
	}
	
}
